import UIKit
//1. create a constant holding any tempature in celsius
let celsiusTemperature: Double = 25.0

//2.Convert the Celsius temperature to Fahrenheit
let fahrenheitTemperature = (celsiusTemperature * 9 / 5) + 32

//3. Print the result for the user showing both the Celsius and Fahrenheit values
print("The Temperature is \(celsiusTemperature)°C, which is \(fahrenheitTemperature)°F.")
